let number1 = 10;
let number2 = 50;

//Code Example of Addtion
let addition = number1 + number2;
console.log("Addition :", addition);

//Code Example of Subtraction
let Subtraction = number1 - number2;
console.log("Subtraction :", Subtraction);

//Code Example of Multiplication
let Multiplication = 3 * 2;
console.log("Multiplication :", Multiplication);

//Code Example of Exponentiation
let Exponentiation = 3 ** 3;
console.log("Exponentiation :", Exponentiation);

//Code Example of Division
let Division = number1 / number2;
console.log("Division :", Division);

//Code Example of Modulus
let Modulus = number1 % number2;
console.log("Modulus :", Modulus);

